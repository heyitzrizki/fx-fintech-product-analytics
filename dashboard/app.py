    section_header(language, "Campaign Simulator")

    options = segment_options(language, targeting)
    col1, col2, col3 = st.columns(3)
    with col1:
        segment_display = st.selectbox("Audience segment", list(options.keys()), key="sim_segment", help=COPY[language]["segment_help"])
        segment = options[segment_display]
    with col2:
        threshold = st.slider("Minimum repeat likelihood", 0.0, 1.0, 0.35, 0.01, key="sim_threshold", help=COPY[language]["threshold_help"])
    with col3:
        uplift = st.slider("Expected campaign uplift", 0.0, 0.30, 0.05, 0.01, format="%.0f%%", help=COPY[language]["uplift_help"])

    selected = select_targets(targeting, segment, threshold)
    if selected.empty:
        st.warning(COPY[language]["empty_selection"])
        return

    baseline = selected["repeat_probability"].sum()
    after = np.minimum(selected["repeat_probability"] * (1 + uplift), 1).sum()
    incremental = after - baseline
    avg_volume = selected["total_volume_90d"].mean()
    incremental_volume = incremental * avg_volume
    fee_proxy = incremental_volume * FEE_RATE_PROXY

    cols = st.columns(5)
    values = [
        ("target_audience", format_number(len(selected)), "Campaign audience"),
        ("baseline_repeat", format_number(baseline), "Before campaign"),
        ("expected_repeat", format_number(after), "After campaign"),
        ("incremental_repeat", format_number(incremental), "Added users"),
        ("fee_proxy", format_krw(fee_proxy), "Simple fee proxy"),
    ]
    for col, (key, value, caption) in zip(cols, values):
        with col:
            label, tooltip = kpi_text(language, key)
            metric_card(label, value, tooltip, caption)

    scenario = pd.DataFrame({"Scenario": ["Before campaign", "After campaign"], "Expected repeat users": [baseline, after]})
    fig = px.bar(scenario, x="Scenario", y="Expected repeat users", text="Expected repeat users", color_discrete_sequence=[COLOR_TEAL], title="Campaign Impact Estimate")
    fig.update_traces(texttemplate="%{text:,.0f}", textposition="outside", cliponaxis=False)
    fig.update_yaxes(title="Expected repeat users")
    st.plotly_chart(style_plotly(fig), width="stretch")

    insight_box(
        "Planning note",
        f"{format_number(incremental)} incremental repeat users",
        f"At {format_percent(uplift * 100)} assumed uplift, this audience may add about {format_krw(incremental_volume)} in transaction volume.",
        action=True,
    )


