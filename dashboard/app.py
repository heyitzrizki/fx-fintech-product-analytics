def render_segment_explorer(language: str, data: dict[str, pd.DataFrame]) -> None:
    targeting = data["targeting"]
    section_header(language, "Segment Explorer")

    filters = st.columns(5)
    with filters[0]:
        probability_range = st.slider("Repeat likelihood", 0.0, 1.0, (0.0, 1.0), 0.01, help=kpi_text(language, "avg_repeat")[1])
    with filters[1]:
        recency_range = st.slider(
            "Days since last transaction",
            int(targeting["recency_days"].min()),
            int(targeting["recency_days"].max()),
            (int(targeting["recency_days"].min()), int(targeting["recency_days"].max())),
        )
    with filters[2]:
        tx_range = st.slider(
            "Recent transactions",
            int(targeting["transactions_90d"].min()),
            int(targeting["transactions_90d"].max()),
            (int(targeting["transactions_90d"].min()), int(targeting["transactions_90d"].max())),
        )
    with filters[3]:
        failed_range = st.slider(
            "Recent failed transaction rate",
            0.0,
            float(targeting["failed_ratio_90d"].max()),
            (0.0, float(targeting["failed_ratio_90d"].max())),
            0.01,
            help=kpi_text(language, "recent_failed_rate")[1],
        )
    with filters[4]:
        options = segment_options(language, targeting)
        segment_display = st.multiselect("Segment", list(options.keys()), default=list(options.keys()))
