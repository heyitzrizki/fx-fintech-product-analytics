        legend=dict(
            orientation="h",
            yanchor="bottom",
            y=1.03,
            xanchor="left",
            x=0,
            font=dict(color=COLOR_TEXT),
        ),
        hoverlabel=dict(bgcolor="white", font_size=12, font_color=COLOR_TEXT),
